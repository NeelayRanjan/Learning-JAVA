package com.learn_Data_types;

public class FloatDouble {

    public static void main(String[] args) {
        float myFloatVal = 5.25F;
        // Use float for decimal numbers with 7 decimals of precision
        // Has a width of 32
        // Important: Add uppercase F to end of number

        double myDoubleVal = 1.125D;
        // Use float for decimal numbers with 16 decimals of precision
        // Has a width of 64
        // Important: Add uppercase D to end of number


        //Examples:
        int myInt = 5/3;
        float myFloat = 5F/3F;
        double myDouble = 5D/3D;
        System.out.println("myInt = " + myInt);
        System.out.println("myFloat = " + myFloat);
        System.out.println("myDouble = " + myDouble);
    }
}
